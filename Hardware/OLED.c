#include "OLED.h"
#include "Delay.h"

static uint8_t i2c_errorCount = 0;

static uint32_t pow10(uint8_t n) {
    uint32_t v = 1;
    while (n--) v *= 10;
    return v;
}

/* ---------- I2C 总线恢复 ---------- */
void I2C_BusRecovery(void) {
    I2C_Cmd(I2C1, DISABLE);
    /* 释放 I2C 复用，切回普通 GPIO */
    GPIO_InitTypeDef g;
    g.GPIO_Mode  = GPIO_Mode_Out_OD;
    g.GPIO_Speed = GPIO_Speed_50MHz;
    g.GPIO_Pin   = GPIO_Pin_6 | GPIO_Pin_7;
    GPIO_Init(GPIOB, &g);

    /* 模拟 SCL 翻转，迫使从机释放 SDA */
    GPIO_SetBits(GPIOB, GPIO_Pin_6);
    for (uint8_t i = 0; i < 9; i++) {
        GPIO_ResetBits(GPIOB, GPIO_Pin_6);
        Delay_us(5);
        GPIO_SetBits(GPIOB, GPIO_Pin_6);
        Delay_us(5);
    }
    /* 产生 STOP 条件: SDA 拉低 → SCL 拉高 → SDA 拉高 */
    GPIO_ResetBits(GPIOB, GPIO_Pin_7);
    Delay_us(5);
    GPIO_SetBits(GPIOB, GPIO_Pin_6);
    Delay_us(5);
    GPIO_SetBits(GPIOB, GPIO_Pin_7);
    Delay_us(5);

    /* 恢复 I2C 复用 */
    g.GPIO_Mode = GPIO_Mode_AF_OD;
    GPIO_Init(GPIOB, &g);

    /* 重新初始化 I2C */
    I2C_InitTypeDef i2c;
    i2c.I2C_Mode                = I2C_Mode_I2C;
    i2c.I2C_ClockSpeed          = 400000;
    i2c.I2C_DutyCycle           = I2C_DutyCycle_2;
    i2c.I2C_Ack                 = I2C_Ack_Enable;
    i2c.I2C_AcknowledgedAddress = I2C_AcknowledgedAddress_7bit;
    i2c.I2C_OwnAddress1         = 0x00;
    I2C_Init(I2C1, &i2c);
    I2C_Cmd(I2C1, ENABLE);

    i2c_errorCount = 0;
}

/* ---------- hardware I2C helpers ---------- */
static int I2C_WriteByte(uint8_t devAddr, uint8_t regAddr, uint8_t data) {
    uint32_t t;
    t = 5000; while (I2C_GetFlagStatus(I2C1, I2C_FLAG_BUSY)) {
        if (--t == 0) break;
    }
    if (t == 0) { i2c_errorCount++; I2C_BusRecovery(); return -1; }

    I2C_GenerateSTART(I2C1, ENABLE);
    t = 5000; while (!I2C_CheckEvent(I2C1, I2C_EVENT_MASTER_MODE_SELECT)) { if (--t == 0) goto err; }
    I2C_Send7bitAddress(I2C1, devAddr, I2C_Direction_Transmitter);
    t = 5000; while (!I2C_CheckEvent(I2C1, I2C_EVENT_MASTER_TRANSMITTER_MODE_SELECTED)) { if (--t == 0) goto err; }
    I2C_SendData(I2C1, regAddr);
    t = 5000; while (!I2C_CheckEvent(I2C1, I2C_EVENT_MASTER_BYTE_TRANSMITTED)) { if (--t == 0) goto err; }
    I2C_SendData(I2C1, data);
    t = 5000; while (!I2C_CheckEvent(I2C1, I2C_EVENT_MASTER_BYTE_TRANSMITTED)) { if (--t == 0) goto err; }
    I2C_GenerateSTOP(I2C1, ENABLE);
    i2c_errorCount = 0;
    return 0;
err:
    i2c_errorCount++;
    I2C_GenerateSTOP(I2C1, ENABLE);
    return -1;
}

static void OLED_WriteCmd(uint8_t cmd) { I2C_WriteByte(OLED_ADDR, 0x00, cmd); }
static void OLED_WriteData(uint8_t dat) { I2C_WriteByte(OLED_ADDR, 0x40, dat); }

