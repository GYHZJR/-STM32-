static void OLED_SetCursor(uint8_t x, uint8_t y) {
    OLED_WriteCmd(0xB0 | y);
    OLED_WriteCmd(0x10 | ((x & 0xF0) >> 4));
    OLED_WriteCmd(0x00 | (x & 0x0F));
}

/* ---------- public API ---------- */
void OLED_Init(void) {
    /* I2C1 init */
    RCC_APB1PeriphClockCmd(RCC_APB1Periph_I2C1, ENABLE);
    RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOB, ENABLE);
    GPIO_InitTypeDef g = {GPIO_Pin_6 | GPIO_Pin_7, GPIO_Speed_50MHz, GPIO_Mode_AF_OD};
    GPIO_Init(GPIOB, &g);

    I2C_InitTypeDef i2c;
    i2c.I2C_Mode = I2C_Mode_I2C;
    i2c.I2C_ClockSpeed = 400000;
    i2c.I2C_DutyCycle = I2C_DutyCycle_2;
    i2c.I2C_Ack = I2C_Ack_Enable;
    i2c.I2C_AcknowledgedAddress = I2C_AcknowledgedAddress_7bit;
    i2c.I2C_OwnAddress1 = 0x00;
    I2C_Init(I2C1, &i2c);
    I2C_Cmd(I2C1, ENABLE);

    /* SSD1306 init sequence */
    Delay_ms(100);
    OLED_WriteCmd(0xAE); // display off
    OLED_WriteCmd(0xD5); OLED_WriteCmd(0x80); // clock div
    OLED_WriteCmd(0xA8); OLED_WriteCmd(0x3F); // mux ratio
    OLED_WriteCmd(0xD3); OLED_WriteCmd(0x00); // display offset
    OLED_WriteCmd(0x40); // start line
    OLED_WriteCmd(0x8D); OLED_WriteCmd(0x14); // charge pump
    OLED_WriteCmd(0xA1); // seg remap
    OLED_WriteCmd(0xC8); // com scan dir
    OLED_WriteCmd(0xDA); OLED_WriteCmd(0x12); // com pins
    OLED_WriteCmd(0x81); OLED_WriteCmd(0xCF); // contrast
    OLED_WriteCmd(0xD9); OLED_WriteCmd(0xF1); // pre-charge
    OLED_WriteCmd(0xDB); OLED_WriteCmd(0x40); // vcom detect
    OLED_WriteCmd(0xA4); // display on resume
    OLED_WriteCmd(0xA6); // normal display
    OLED_WriteCmd(0xAF); // display on
}

void OLED_Clear(void) {
    uint8_t i, j;
    for (i = 0; i < 8; i++) {
        OLED_SetCursor(0, i);
        for (j = 0; j < 128; j++) OLED_WriteData(0x00);
    }
}

void OLED_ShowChar(uint8_t x, uint8_t y, uint8_t chr) {
    uint8_t i;
    OLED_SetCursor(x, y);
    for (i = 0; i < 6; i++) OLED_WriteData(F6x8[chr - 0x20][i]);
}

void OLED_ShowString(uint8_t x, uint8_t y, const char *str) {
    uint8_t i = 0;
    while (str[i]) {
        OLED_ShowChar(x + i * 6, y, str[i]);
        i++;
        if (x + i * 6 > 122) break;
    }
}

void OLED_ShowNum(uint8_t x, uint8_t y, uint32_t num, uint8_t len) {
    int8_t i;
    for (i = len - 1; i >= 0; i--) {
        OLED_ShowChar(x + i * 6, y, '0' + (num % 10));
        num /= 10;
    }
}

void OLED_ShowSignedNum(uint8_t x, uint8_t y, int32_t num, uint8_t len) {
    if (num < 0) { OLED_ShowChar(x, y, '-'); num = -num; }
    else OLED_ShowChar(x, y, ' ');
    OLED_ShowNum(x + 6, y, (uint32_t)num, len - 1);
}

void OLED_ShowFloat(uint8_t x, uint8_t y, float num, uint8_t intLen, uint8_t decLen) {
    if (num < 0) { OLED_ShowChar(x, y, '-'); num = -num; }
    else OLED_ShowChar(x, y, ' ');
    uint32_t intPart = (uint32_t)num;
    OLED_ShowNum(x + 6, y, intPart, intLen);
    OLED_ShowChar(x + 6 * (intLen + 1), y, '.');
    uint32_t decPart = (uint32_t)((num - intPart) * pow10(decLen) + 0.5);
    OLED_ShowNum(x + 6 * (intLen + 1) + 6, y, decPart, decLen);
}
